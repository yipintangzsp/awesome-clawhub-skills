#!/bin/bash
# =============================================================================
# 手动安装 Crontab 脚本
# =============================================================================
# 如果自动安装失败，请运行此脚本
# =============================================================================

CRONTAB_FILE="$HOME/.openclaw/workspace/crontab"

echo "=========================================="
echo "  Crontab 手动安装"
echo "=========================================="
echo ""
echo "请执行以下命令安装 crontab 配置："
echo ""
echo "  crontab $CRONTAB_FILE"
echo ""
echo "或者复制粘贴以下内容到终端："
echo ""
echo "--- 开始复制 ---"
cat "$CRONTAB_FILE" | pbcopy
echo "✅ 已复制到剪贴板"
echo ""
echo "然后运行："
echo "  pbpaste | crontab -"
echo ""
echo "=========================================="
echo "验证安装："
echo "  crontab -l | grep auto-publish"
echo "=========================================="
